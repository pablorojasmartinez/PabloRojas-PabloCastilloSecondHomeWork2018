    private void jtfGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jtfGuardarActionPerformed
        String nombre = jtfNombre.getText();
        //String nombre2 = "";
        //if (nombre.length() > 11) {
        //  nombre2=nombre.substring(0, 10);
        // JOptionPane.showMessageDialog(null, "El tamaño del nombre es mayor a lo permitido");
        //}

        int anno = Integer.parseInt(jtfAnno.getText());
        int kilo = Integer.parseInt(jtfKilo.getText());

        boolean americano = false;
        if (jRadioSi.isSelected()) {
            americano = true;
        }
        int serie = Integer.parseInt(jtfSerie.getText());

        try {
            if (nombre.length() < 10) {
                Car automovil = new Car(nombre, anno, kilo, americano, serie);

                BusinessCar businessAutomovil = new BusinessCar();
                if (!businessAutomovil.check(serie)) {
                    if (businessAutomovil.insertarAuto(automovil)) {
                        JOptionPane.showMessageDialog(null, "Saved car");
                        jtfNombre.setText("");
                        jtfAnno.setText("");
                        jtfKilo.setText("");
                        jtfSerie.setText("");

                    } else {
                        JOptionPane.showMessageDialog(null, "Error! Car no saved");

                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Already existing car");
                    jtfSerie.setText("");

                }
            } else {
                JOptionPane.showMessageDialog(null, "The size of the name exceeds the allowed");
                jtfNombre.setText("");
            }
        } catch (IOException ex) {
            Logger.getLogger(InsertCar.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jtfGuardarActionPerformed



        int anno = Integer.parseInt(jtfAnno.getText());
        boolean americano = false;
        if (jRadioSi.isSelected()) {
            americano = true;
        }

        try {
            // Car car = new Car(nombre, anno, americano);
            car.setAmerican(americano);
            car.setName(nombre);
            car.setYear(anno);

            BusinessCar businessAutomovil = new BusinessCar();
            businessAutomovil.delete(car.getId());
            businessAutomovil.insertarAuto(car);
            JOptionPane.showMessageDialog(null, car);
        } catch (IOException ex) {
            Logger.getLogger(Update.class.getName()).log(Level.SEVERE, null, ex);
        }

